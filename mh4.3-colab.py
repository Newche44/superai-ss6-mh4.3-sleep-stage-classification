def predict_subject(subject_path, model, feature_cols, le, smooth=True):
    """
    โหลด segment files ของ 1 subject แล้ว predict
    Returns: dict {segment_id: predicted_label}
    """
    seg_files = sorted(
        glob.glob(os.path.join(subject_path, '*.xlsx')) +
        glob.glob(os.path.join(subject_path, '*.csv'))
    )
    if not seg_files:
        return {}

    seg_features = []
    seg_ids      = []

    for i, sf in enumerate(seg_files):
        try:
            seg_df = pd.read_excel(sf) if sf.endswith('.xlsx') else pd.read_csv(sf)
        except:
            continue
        feat = extract_features(seg_df, seg_idx=i, total_segs=len(seg_files))
        seg_features.append(feat)
        seg_id = os.path.splitext(os.path.basename(sf))[0]
        seg_ids.append(seg_id)

    if not seg_features:
        return {}

    X_test_s = pd.DataFrame(seg_features).reindex(columns=feature_cols, fill_value=0)
    preds_int = model.predict(X_test_s).argmax(axis=1)

    if smooth and len(preds_int) > 3:
        # Temporal smoothing ด้วย median filter (window=3)
        preds_int = median_filter(preds_int, size=3)

    pred_labels = le.inverse_transform(preds_int)
    return dict(zip(seg_ids, pred_labels))


