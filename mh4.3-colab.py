def safe_array(series):
    """แปลง Series → numpy array โดย drop NaN และป้องกัน empty array"""
    arr = series.values.astype(float)
    arr = arr[~np.isnan(arr)]
    return arr if len(arr) > 0 else np.zeros(1)


def band_power(fft_vals, freqs, f_low, f_high):
    """คำนวณ power ใน frequency band"""
    mask = (freqs >= f_low) & (freqs < f_high)
    return np.sum(fft_vals[mask] ** 2)


def extract_features(seg, seg_idx=0, total_segs=1):
    """
    Extract feature vector จาก 1 segment (480 rows = 30 วินาที)
    Returns: dict of features
    """
    features = {}

    # ──────────────────────────────────────────────
    # 1. Statistical Features (ทุก channel)
    # ──────────────────────────────────────────────
    channels = ['BVP', 'ACC_X', 'ACC_Y', 'ACC_Z', 'TEMP', 'EDA', 'HR', 'IBI']
    for col in channels:
        if col not in seg.columns:
            continue
        x = safe_array(seg[col])
        features[f'{col}_mean']   = np.mean(x)
        features[f'{col}_std']    = np.std(x)
        features[f'{col}_min']    = np.min(x)
        features[f'{col}_max']    = np.max(x)
        features[f'{col}_median'] = np.median(x)
        features[f'{col}_q25']    = np.percentile(x, 25)
        features[f'{col}_q75']    = np.percentile(x, 75)
        features[f'{col}_iqr']    = np.percentile(x, 75) - np.percentile(x, 25)
        features[f'{col}_skew']   = stats.skew(x)
        features[f'{col}_kurt']   = stats.kurtosis(x)
        features[f'{col}_rms']    = np.sqrt(np.mean(x ** 2))
        features[f'{col}_range']  = np.max(x) - np.min(x)

    # ──────────────────────────────────────────────
    # 2. BVP Frequency Domain (FFT)
    # ──────────────────────────────────────────────
    bvp = np.nan_to_num(seg['BVP'].values.astype(float)) if 'BVP' in seg.columns else np.zeros(SEG_LEN)
    fft_vals = np.abs(fft(bvp))[:len(bvp) // 2]
    freqs    = fftfreq(len(bvp), d=1 / FS)[:len(bvp) // 2]

    vlf   = band_power(fft_vals, freqs, 0.003, 0.04)
    lf    = band_power(fft_vals, freqs, 0.04,  0.15)
    hf    = band_power(fft_vals, freqs, 0.15,  0.40)
    total = vlf + lf + hf + 1e-10

    features['bvp_vlf']     = vlf
    features['bvp_lf']      = lf
    features['bvp_hf']      = hf
    features['bvp_lf_hf']   = lf / (hf + 1e-10)   # สำคัญสำหรับ REM detection
    features['bvp_lf_norm'] = lf / total
    features['bvp_hf_norm'] = hf / total

    # ──────────────────────────────────────────────
    # 3. HRV Features (จาก IBI)
    # ──────────────────────────────────────────────
    if 'IBI' in seg.columns:
        ibi = safe_array(seg['IBI'])
        ibi = ibi[ibi > 0]   # กรอง 0 ออก
        if len(ibi) > 1:
            diff_ibi = np.diff(ibi)
            features['hrv_sdnn']     = np.std(ibi)
            features['hrv_rmssd']    = np.sqrt(np.mean(diff_ibi ** 2))
            features['hrv_pnn50']    = np.mean(np.abs(diff_ibi) > 50)
            features['hrv_mean_ibi'] = np.mean(ibi)
            features['hrv_cv']       = np.std(ibi) / (np.mean(ibi) + 1e-10)
        else:
            for k in ['hrv_sdnn', 'hrv_rmssd', 'hrv_pnn50', 'hrv_mean_ibi', 'hrv_cv']:
                features[k] = 0.0

    # ──────────────────────────────────────────────
    # 4. ACC Magnitude & Motion Features
    # ──────────────────────────────────────────────
    if all(c in seg.columns for c in ['ACC_X', 'ACC_Y', 'ACC_Z']):
        ax = np.nan_to_num(seg['ACC_X'].values.astype(float))
        ay = np.nan_to_num(seg['ACC_Y'].values.astype(float))
        az = np.nan_to_num(seg['ACC_Z'].values.astype(float))
        mag = np.sqrt(ax**2 + ay**2 + az**2)

        features['acc_mag_mean'] = np.mean(mag)
        features['acc_mag_std']  = np.std(mag)
        features['acc_mag_max']  = np.max(mag)
        features['acc_sma']      = np.mean(np.abs(ax) + np.abs(ay) + np.abs(az))

        # Zero-crossing rate (proxy for movement)
        for name, arr in [('ACC_X', ax), ('ACC_Y', ay), ('ACC_Z', az)]:
            centered = arr - np.mean(arr)
            zcr = np.sum(np.diff(np.sign(centered)) != 0) / len(centered)
            features[f'{name}_zcr'] = zcr

    # ──────────────────────────────────────────────
    # 5. EDA — slope & peak count
    # ──────────────────────────────────────────────
    if 'EDA' in seg.columns:
        eda = np.nan_to_num(seg['EDA'].values.astype(float))
        t = np.arange(len(eda))
        slope, intercept = np.polyfit(t, eda, 1) if np.std(eda) > 0 else (0, 0)
        features['eda_slope'] = slope
        # peak count (SCR)
        peaks, _ = signal.find_peaks(eda, height=np.mean(eda))
        features['eda_peak_count'] = len(peaks)

    # ──────────────────────────────────────────────
    # 6. TEMP — slope (body temp ลดลงเมื่อเข้า N3)
    # ──────────────────────────────────────────────
    if 'TEMP' in seg.columns:
        temp = np.nan_to_num(seg['TEMP'].values.astype(float))
        t = np.arange(len(temp))
        slope, _ = np.polyfit(t, temp, 1) if np.std(temp) > 0 else (0, 0)
        features['temp_slope'] = slope

    # ──────────────────────────────────────────────
    # 7. Contextual / Temporal Features
    # ──────────────────────────────────────────────
    features['segment_idx']      = seg_idx
    features['segment_idx_norm'] = seg_idx / max(total_segs - 1, 1)
    features['total_segments']   = total_segs

    return features


